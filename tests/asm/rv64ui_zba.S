/*
 * rv64ui_zba.S - Zba extension tests (address generation)
 *
 * Tests sh1add, sh2add, sh3add: rd = (rs1 << N) + rs2
 */

#include "test_macros.h"

    .section .text.init
    .globl _start
_start:

    # ------------------------------------------------------------------
    # Test 1: sh1add rd, rs1, rs2 => (rs1 << 1) + rs2
    # ------------------------------------------------------------------
    li   a0, 10             # rs1
    li   a1, 5              # rs2
    sh1add a2, a0, a1       # (10 << 1) + 5 = 25
    TEST_CASE 1, a2, 25

    # ------------------------------------------------------------------
    # Test 2: sh2add rd, rs1, rs2 => (rs1 << 2) + rs2
    # ------------------------------------------------------------------
    li   a0, 10
    li   a1, 5
    sh2add a2, a0, a1       # (10 << 2) + 5 = 45
    TEST_CASE 2, a2, 45

    # ------------------------------------------------------------------
    # Test 3: sh3add rd, rs1, rs2 => (rs1 << 3) + rs2
    # ------------------------------------------------------------------
    li   a0, 10
    li   a1, 5
    sh3add a2, a0, a1       # (10 << 3) + 5 = 85
    TEST_CASE 3, a2, 85

    # ------------------------------------------------------------------
    # Test 4: sh1add with zero rs2
    # ------------------------------------------------------------------
    li   a0, 100
    li   a1, 0
    sh1add a2, a0, a1       # (100 << 1) + 0 = 200
    TEST_CASE 4, a2, 200

    # ------------------------------------------------------------------
    # Test 5: sh2add with large values
    # ------------------------------------------------------------------
    li   a0, 0x10000000
    li   a1, 0x1
    sh2add a2, a0, a1       # (0x10000000 << 2) + 1 = 0x40000001
    TEST_CASE 5, a2, 0x40000001

    # ------------------------------------------------------------------
    # Test 6: sh3add with negative rs1
    # ------------------------------------------------------------------
    li   a0, -1
    li   a1, 100
    sh3add a2, a0, a1       # (-1 << 3) + 100 = -8 + 100 = 92
    TEST_CASE 6, a2, 92

    PASS

    .section .tohost,"aw",@progbits
    .globl tohost
tohost:
    .dword 0
