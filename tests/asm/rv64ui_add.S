/*
 * rv64ui_add.S - Basic ADD/ADDI/ADDW/ADDIW tests
 *
 * Tests arithmetic instructions with various operands including
 * zero, negative, MAX, MIN values.
 */

#include "test_macros.h"

    .section .text.init
    .globl _start
_start:

    # ------------------------------------------------------------------
    # Test 1: ADD zero + zero = 0
    # ------------------------------------------------------------------
    li   a0, 0
    li   a1, 0
    add  a2, a0, a1
    TEST_CASE 1, a2, 0

    # ------------------------------------------------------------------
    # Test 2: ADD positive + positive
    # ------------------------------------------------------------------
    li   a0, 100
    li   a1, 200
    add  a2, a0, a1
    TEST_CASE 2, a2, 300

    # ------------------------------------------------------------------
    # Test 3: ADD positive + negative
    # ------------------------------------------------------------------
    li   a0, 50
    li   a1, -30
    add  a2, a0, a1
    TEST_CASE 3, a2, 20

    # ------------------------------------------------------------------
    # Test 4: ADD negative + negative
    # ------------------------------------------------------------------
    li   a0, -100
    li   a1, -200
    add  a2, a0, a1
    TEST_CASE 4, a2, -300

    # ------------------------------------------------------------------
    # Test 5: ADDI with positive immediate
    # ------------------------------------------------------------------
    li   a0, 10
    addi a2, a0, 2047
    TEST_CASE 5, a2, 2057

    # ------------------------------------------------------------------
    # Test 6: ADDI with negative immediate
    # ------------------------------------------------------------------
    li   a0, 100
    addi a2, a0, -50
    TEST_CASE 6, a2, 50

    # ------------------------------------------------------------------
    # Test 7: ADDI with zero
    # ------------------------------------------------------------------
    li   a0, 42
    addi a2, a0, 0
    TEST_CASE 7, a2, 42

    # ------------------------------------------------------------------
    # Test 8: ADDW (32-bit add, sign-extended to 64)
    # ------------------------------------------------------------------
    li   a0, 0x7FFFFFFF     # INT32_MAX
    li   a1, 1
    addw a2, a0, a1         # wraps to 0x80000000, sign-extends to -2147483648
    TEST_CASE 8, a2, -2147483648

    # ------------------------------------------------------------------
    # Test 9: ADDIW (32-bit add immediate, sign-extended)
    # ------------------------------------------------------------------
    li   a0, 0x7FFFFFFD
    addiw a2, a0, 2
    TEST_CASE 9, a2, 0x7FFFFFFF

    # ------------------------------------------------------------------
    # Test 10: ADD large 64-bit values
    # ------------------------------------------------------------------
    li   a0, 0x100000000     # 2^32
    li   a1, 0x200000000     # 2*2^32
    add  a2, a0, a1
    TEST_CASE 10, a2, 0x300000000

    PASS

    .section .tohost,"aw",@progbits
    .globl tohost
tohost:
    .dword 0
